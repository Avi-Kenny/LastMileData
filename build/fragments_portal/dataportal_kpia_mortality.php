<h1>KPI Assessment (Konobo, Aug 2014) - Mortality <span style="font-size:60%">(updated: 4/12/2015)</span></h1>
<hr>

<h3>Highlights</h3>
<ul>
    <li>The neonatal mortality rate in Konobo was assessed at 28.8 per 1,000 live births, a similar rate to the national average of 26, as measured by the 2013 Liberia Demographic and Health Survey (DHS).</li>
    <li>The infant mortality rate (48.2) and child mortality rate (28.0) appear to be lower than those in the rest of the country.</li>
</ul>
<br>

