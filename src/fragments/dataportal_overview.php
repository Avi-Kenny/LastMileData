<div id="overview">
    
    <h3>Notes</h3>
    <ul>
        <li>
            <b>Data updates.</b>
            The <i>Data Portal</i> is updated on the 12th of each month.
        </li>
        <li>
            <b>Data usage.</b>
            Any information within the <i>Data Portal</i> can be used internally or externally. Feel free to consult the M&E team if there is any uncertainty around how to best contextualize or frame the data.
        </li>
        <li>
            <b>Screen resolution.</b>
            A minimum screen resolution of 1024x768 is required to properly display content.
        </li>
    </ul>
    
    <h3>Contents</h3>
    <ul>
        <li>
            <b>Monthly dashboards.</b>
            Contains a number of reports that are updated on a monthly basis.
        </li>
        <li>
            <b>KPI Assessment.</b>
            Data from the KPI Assessment, a short survey administered by FHWs in August of 2014 in Konobo.
        </li>
        <li>
            <b>Liberia stats.</b>
            Population data for the communities in Liberia, and selections of data from the 2013 Demographic and Health Survey (DHS) of Liberia.
        </li>
        <li>
            <b>M&E team tools</b>
            Tools for use by our Monitoring & Evaluation team.
        </li>
    </ul>
    
    <h3>Coming soon..</h3>
    <ul>
        <li>
            <b>Maps.</b>
            Maps and GIS data from our new scale-up sites (Rivercess, Gboe-Ploe).
        </li>
    </ul>
    
</div>

