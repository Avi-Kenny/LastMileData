<h1>Updates</h1>
<hr>

<div id="overview">
    
    <h3>May 2015</h3>
    <ul>
        <li>
            <b>Access control.</b>
            Created an “access control” system, whereby different users see different subsets of Data Portal reports based on their role. For example, members of the Rivercess County Health Team (CHT) will be able to see data from Rivercess, but not from Grand Gedeh.
        </li>
        <li>
            <b>Konobo Monthly Report.</b>
            Expanded report with data on births, deaths, and movements.
        </li>
        <li>
            <b>Short link.</b>
            Go to LastMileData.org/portal to access the Data Portal.
        </li>
    </ul>
    
    <h3>April 2015</h3>
    <ul>
        <li>
            <b>Konobo Monthly Report.</b>
            Created to track monthly progress in Konobo on a variety of input, output, process, and impact indicators.
        </li>
        <li>
            <b>KPI Assessment.</b>
            Data from the KPI Assessment, a short survey administered by Konobo FHWs in August of 2014, was migrated to the Data Portal.
        </li>
        <li>
            <b>Organization.</b>
            <i>Data Portal</i> sidebar was reorganized such that all reports are placed within a category, to allow for easier navigation.
        </li>
        <li>
            <b>Portal updates.</b>
            Portal updates page was created to log changes made to the <i>Data Portal</i>.
        </li>
        <li>
            <b>DHS 2013.</b>
            Select data from the 2013 Liberian Demographic and Health Survey (DHS) was added to the Data Portal for easy access.
        </li>
        <li>
            <b>M&E Team Tools</b>
            Tools and reports used by the M&E team were migrated onto the Data Portal.
        </li>
    </ul>
    
    <h3>March 2015</h3>
    <ul>
        <li>
            <b>Executive dashboard.</b>
            Cross-team dashboard, with data from programs, HR, development, and finance.
        </li>
        <li>
            <b>Ebola Activities.</b>
            Ebola education/screening numbers, training data.
        </li>
        <li>
            <b>Liberia population.</b>
            Population data for the communities in Liberia.
        </li>
    </ul>
    
</div>

