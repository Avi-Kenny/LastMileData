<h1>KPI Assessment (Konobo, Aug 2014) - Vaccination <span style="font-size:60%">(updated: 4/12/2015)</span></h1>
<hr>

<h3>Highlights</h3>
<ul>
    <li>Out of all children 9-23 months whose mothers had their vaccine cards, only 24.1% were fully vaccinated, less than half of the DHS rural average of 49.4%.</li>
    <li>Out of all children 0-9 months with vaccine cards, 39.7% were on track for full vaccination.</li>
    <li>Actual rates may be lower, given that children with vaccine cards are likely to have received more vaccinations than those without cards, and that 46.6% of children did not have vaccine cards.</li>
</ul>
