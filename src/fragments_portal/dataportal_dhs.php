<h3>Rivercess stats</h3>
<ul>
    <li>Rivercess is the poorest county in Liberia, with 71.3% of the population falling within the bottom 20% income bracket of the country.</li>
    <li>Also the lowest in terms of female education (only 9.9% of women attended high school).</li>
    <li>Lowest median age at first birth (17.8 years).</li>
    <li>73.7% of women reported "distance to health facility" as a serious problem in accessing healthcare, more than in any other county.</li>
    <li>Lowest full-vaccination rate in Liberia, at 33.1%.</li>
    <li>Highest prevalence of ARI (12.2%), and the second-highest prevalence of diarrhea (32.1%).</li>
    <li>Lowest treatment rate for children with fever (41.8%).</li>
</ul>
