<h1>KPI Assessment (Konobo, Aug 2014) - Antenatal care <span style="font-size:60%">(updated: 4/12/2015)</span></h1>
<hr>

<h3>Highlights</h3>
<ul>
    <li>Out of women who completed a pregnancy in 2014, 94.9% reported having received at least one ANC visit, while 60.1% received at least four ANC visits (the WHO recommendation).</li>
    <li>ANC visitation rates appear to have generally increased since 2010, but the bulk of improvements appear to have predated LMH ANC programming; it is unclear whether trends reflect recall bias, historical trends, or program effects.</li>
</ul>
