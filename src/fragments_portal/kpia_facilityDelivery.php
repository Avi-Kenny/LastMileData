<h1>KPI Assessment (Konobo, Aug 2014) - Facility delivery <span style="font-size:60%">(updated: 4/12/2015)</span></h1>
<hr>

<h3>Highlights</h3>
<ul>
    <li>Of all deliveries in 2014, 78.9% were reported to have occurred in a health facility.</li>
    <li>Facility-based delivery rates appear to have increased in recent years (even before the start of the program), although methods of historical assessment are potentially skewed by recall bias and social desirability bias.</li>
    <li>The rate has remained high, even after the Ebola outbreak.</li>
</ul>
